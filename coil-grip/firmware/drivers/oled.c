#include "board.h"
#include "registers.h"

#define OLED_WIDTH   128U
#define OLED_HEIGHT   64U
#define OLED_PAGES    (OLED_HEIGHT / 8)

static uint8_t g_fb[OLED_WIDTH * OLED_PAGES];

/* ---- Bit-bang SPI ----------------------------------------------------- */
static void oled_sck_lo(void) { GPIOB->BSRR = BIT(PIN_PB8 + 16); }
static void oled_sck_hi(void) { GPIOB->BSRR = BIT(PIN_PB8); }
static void oled_sda_lo(void) { GPIOB->BSRR = BIT(PIN_PB9 + 16); }
static void oled_sda_hi(void) { GPIOB->BSRR = BIT(PIN_PB9); }
static void oled_cs_lo(void)  { GPIOB->BSRR = BIT(PIN_PB5 + 16); }
static void oled_cs_hi(void)  { GPIOB->BSRR = BIT(PIN_PB5); }
static void oled_dc_lo(void)  { GPIOA->BSRR = BIT(PIN_PA15 + 16); }  /* command */
static void oled_dc_hi(void)  { GPIOA->BSRR = BIT(PIN_PA15); }      /* data */

static void oled_spi_write(uint8_t b)
{
    for (int i = 7; i >= 0; i--) {
        oled_sck_lo();
        if (b & BIT(i)) oled_sda_hi(); else oled_sda_lo();
        oled_sck_hi();
    }
}

static void oled_cmd(uint8_t c)
{
    oled_dc_lo();
    oled_cs_lo();
    oled_spi_write(c);
    oled_cs_hi();
}

static void oled_data(uint8_t d)
{
    oled_dc_hi();
    oled_cs_lo();
    oled_spi_write(d);
    oled_cs_hi();
}

